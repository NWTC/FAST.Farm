       CALL SetErrStat(ErrID_Fatal, 'Error allocating mask1.', ErrStat, ErrMsg,RoutineName)
       RETURN
    END IF
    mask1 = .TRUE. 
      IF (SIZE(OutData%Z_high)>0) OutData%Z_high = UNPACK(ReKiBuf( Re_Xferred:Re_Xferred+(SIZE(OutData%Z_high))-1 ), mask1, 0.0_ReKi )
